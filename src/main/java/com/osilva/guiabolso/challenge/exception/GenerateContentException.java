package com.osilva.guiabolso.challenge.exception;

public class GenerateContentException extends Exception {

    public GenerateContentException() {
    }

    public GenerateContentException(String s) {
        super(s);
    }

    public GenerateContentException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public GenerateContentException(Throwable throwable) {
        super(throwable);
    }
}
